import random

class BaseMessage:
    @classmethod
    def _get_parts(cls) -> list[list[str]]:
        parts = []
        for name, value in cls.__dict__.items():
            if isinstance(value, list) and not name.startswith('_'):
                parts.append(value)
        return parts

    def build(self) -> str:
        parts = [random.choice(lst) for lst in self._get_parts()]
        return ' '.join(parts)

