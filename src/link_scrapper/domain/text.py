# ---------------------------
# Базовый класс
# ---------------------------
class BaseMessage:
    @classmethod
    def _get_parts(cls) -> list[list[str]]:
        parts = []
        for name, value in cls.__dict__.items():
            if isinstance(value, list) and not name.startswith('_'):
                parts.append(value)
        return parts

    def build(self) -> str:
        parts = []
        for lst in self._get_parts():
            parts.append(random.choice(lst))
        return ' '.join(parts)

